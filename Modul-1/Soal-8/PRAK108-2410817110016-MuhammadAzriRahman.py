putaran = 5
jarak = 14
keliling = jarak / putaran
phi = 3.14
jarijari = (keliling) / (2 * phi) 
    
print("Diketahui :")
print(f"Pak Dengklek mengelilingi taman = {putaran} Putaran")
print(f"Jarak tempuh Pak Dengklek = {jarak} Kilometer")
print("Jawaban :")
print("Jari-jari taman yang dikelilingi Pak Dengklek adalah %.2f Kilometer"%jarijari)